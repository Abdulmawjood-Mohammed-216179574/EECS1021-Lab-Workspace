package Default2;
public class LogicalOperations1 {

    public static void main(String[] args) {
        // There are 3 relational operators
        // Each evaluating to true and false
        // 1. logical negation (not)  !
        // 2. logical conjunction (and)  &&
        // 3. logical disjunction (or)  ||

        boolean p = true;
        // can also use boolean negation = !p, to put !p in a variable
        System.out.println("Logical negation of p being " + p + " is: " + !p);
        p = false;
        System.out.println("Logical negation of p being " + p + " is: " + !p);




    }

}